export const getProductById = (id: string): Product | undefined => {
  return products.find((p) => p.id === id);
};

export const getProductsByCategory = (categorySlug: string): Product[] => {
  return products.filter((p) => p.category === categorySlug || p.subcategory === categorySlug);
};

export const getFeaturedProducts = (): Product[] => {
  return products.filter((p) => p.badges?.includes('bestseller') || p.badges?.includes('offer')).slice(0, 8);
};

export const getNewProducts = (): Product[] => {
  return products.filter((p) => p.badges?.includes('new')).slice(0, 8);
};

export const getRelatedProducts = (productId: string, limit = 4): Product[] => {
  const product = getProductById(productId);
  if (!product) return [];
  
  return products
    .filter((p) => p.id !== productId && (p.category === product.category || p.brand === product.brand))
    .slice(0, limit);
};
